// Default language and mapping to SpeechSynthesis voices
const fallbackLanguage = 'en';
const defaultArtifactMap = 'assets/world-map.png';
const languageVoiceMap = {
  en: 'en-US',
  es: 'es-ES',
  zh: 'zh-CN',
  fr: 'fr-FR',
  ar: 'ar-SA'
};
let currentLanguage = localStorage.getItem('mechanicalManiacsLanguage') || fallbackLanguage;
if (!translations[currentLanguage]) {
  currentLanguage = fallbackLanguage;
}

let artifactModal;
let modalImage;
let modalNumber;
let modalTitle;
let modalDate;
let modalDescription;
let modalMapImage;
let activeArtifactCard = null;
let currentUtterance = null;
const speechSupported = typeof window !== 'undefined' && 'speechSynthesis' in window;

// Helpers for translation lookup
const hasTranslation = (lang, key) => Object.prototype.hasOwnProperty.call(translations[lang] || {}, key);

const resolveTranslation = (key) => {
  if (!key) return '';
  if (hasTranslation(currentLanguage, key)) {
    return translations[currentLanguage][key];
  }
  if (hasTranslation(fallbackLanguage, key)) {
    return translations[fallbackLanguage][key];
  }
  return key;
};

const formatTranslation = (key, replacements = {}) => {
  let text = resolveTranslation(key);
  Object.entries(replacements).forEach(([token, value]) => {
    const regex = new RegExp(`{${token}}`, 'g');
    text = text.replace(regex, value);
  });
  return text;
};

// Apply translations to DOM + adjust direction
const applyTranslations = () => {
  document.documentElement.lang = currentLanguage === 'zh' ? 'zh-Hans' : currentLanguage;
  document.documentElement.dir = currentLanguage === 'ar' ? 'rtl' : 'ltr';
  document.body.dataset.language = currentLanguage;
  document.body.classList.toggle('rtl', currentLanguage === 'ar');
  if (typeof navToggle !== 'undefined' && navToggle) {
    navToggle.setAttribute('aria-label', resolveTranslation('nav.toggle'));
  }

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    if (!key) return;
    const translation = resolveTranslation(key);
    if (!translation) return;
    if (el.dataset.i18nAttr) {
      el.setAttribute(el.dataset.i18nAttr, translation);
    } else {
      el.innerHTML = translation;
    }
  });

  if (artifactModal && artifactModal.dataset.open === 'true' && activeArtifactCard) {
    populateArtifactModal(activeArtifactCard);
  }
};

// Highlight active language chip
const updateLanguageButtons = () => {
  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.classList.toggle('active', btn.dataset.lang === currentLanguage);
  });
};

// Switch languages, persist choice, and refresh UI
const setLanguage = (lang) => {
  if (!translations[lang]) {
    lang = fallbackLanguage;
  }
  if (currentLanguage === lang) {
    return;
  }
  if (speechSupported) {
    window.speechSynthesis.cancel();
    currentUtterance = null;
  }
  currentLanguage = lang;
  localStorage.setItem('mechanicalManiacsLanguage', currentLanguage);
  applyTranslations();
  updateLanguageButtons();
};

const navToggle = document.querySelector('.nav-toggle');
const nav = document.querySelector('.site-nav');
const navLinks = document.querySelectorAll('.site-nav a');

// Mobile nav toggle
const setNavigationState = (isOpen) => {
  document.body.dataset.navOpen = isOpen ? 'true' : 'false';
  if (nav) {
    nav.dataset.open = isOpen ? 'true' : 'false';
  }
  if (navToggle) {
    navToggle.setAttribute('aria-expanded', String(isOpen));
    navToggle.setAttribute('aria-label', resolveTranslation('nav.toggle'));
  }
};

if (navToggle && nav) {
  setNavigationState(false);
  navToggle.addEventListener('click', () => {
    const currentlyOpen = nav.dataset.open === 'true';
    setNavigationState(!currentlyOpen);
  });

  navLinks.forEach((link) => {
    link.addEventListener('click', () => {
      if (window.innerWidth <= 900) {
        setNavigationState(false);
      }
    });
  });

  window.addEventListener('resize', () => {
    if (window.innerWidth > 900) {
      setNavigationState(false);
    }
  });
}

const artifactCards = document.querySelectorAll('[data-artifact]');
artifactModal = document.querySelector('.artifact-modal');
if (artifactModal) {
  modalImage = artifactModal.querySelector('#artifact-modal-image');
  modalNumber = artifactModal.querySelector('#artifact-modal-number');
  modalTitle = document.getElementById('artifact-modal-title');
  modalDate = document.getElementById('artifact-modal-date');
  modalDescription = document.getElementById('artifact-modal-description');
  modalMapImage = document.getElementById('artifact-map-image');
}

// Fill modal with translated artifact content
const populateArtifactModal = (card) => {
  if (!artifactModal || !card) return;
  const titleText = resolveTranslation(card.dataset.modalTitle);
  const dateText = resolveTranslation(card.dataset.modalDate);
  const descriptionText = resolveTranslation(card.dataset.modalDescription);
  const artifactId = card.dataset.id || '';
  const mapSrc = card.dataset.map || defaultArtifactMap;

  if (modalImage) {
    modalImage.src = card.dataset.detail || '';
    modalImage.alt = formatTranslation('artifacts.modal.imageAlt', { title: titleText });
  }
  if (modalNumber) {
    modalNumber.textContent = artifactId ? `#${artifactId}` : '';
  }
  if (modalTitle) {
    modalTitle.innerHTML = titleText;
  }
  if (modalDate) {
    modalDate.innerHTML = dateText;
  }
  if (modalDescription) {
    modalDescription.innerHTML = descriptionText;
  }
  if (modalMapImage) {
    modalMapImage.src = mapSrc;
  }
};

const openArtifactModal = (card) => {
  if (!artifactModal) return;
  activeArtifactCard = card;
  populateArtifactModal(card);
  artifactModal.dataset.open = 'true';
  artifactModal.setAttribute('aria-hidden', 'false');
  document.body.dataset.modalOpen = 'true';
};

const closeArtifactModal = () => {
  if (!artifactModal) return;
  if (speechSupported) {
    window.speechSynthesis.cancel();
    currentUtterance = null;
  }
  artifactModal.dataset.open = 'false';
  artifactModal.setAttribute('aria-hidden', 'true');
  document.body.dataset.modalOpen = 'false';
  activeArtifactCard = null;
};

if (artifactCards.length && artifactModal) {
  artifactCards.forEach((card) => {
    card.addEventListener('click', () => openArtifactModal(card));
  });

  const closeBtn = artifactModal.querySelector('.artifact-modal-close');
  const backdrop = artifactModal.querySelector('.artifact-modal-backdrop');
  [closeBtn, backdrop].forEach((el) => {
    if (el) {
      el.addEventListener('click', closeArtifactModal);
    }
  });

  document.addEventListener('keydown', (event) => {
    if (event.key === 'Escape' && artifactModal.dataset.open === 'true') {
      closeArtifactModal();
    }
  });
}

const languageButtons = document.querySelectorAll('.lang-btn');
languageButtons.forEach((btn) => {
  btn.addEventListener('click', () => {
    const lang = btn.dataset.lang || fallbackLanguage;
    setLanguage(lang);
  });
});

applyTranslations();
updateLanguageButtons();

const yearSpan = document.getElementById('year');
if (yearSpan) {
  yearSpan.textContent = new Date().getFullYear().toString();
}

const speakButton = document.querySelector('.artifact-tts-btn');
// Text-to-speech using current language voice
const speakArtifactDescription = () => {
  if (!speechSupported || !modalDescription) return;
  const text = modalDescription.textContent.trim();
  if (!text) return;
  window.speechSynthesis.cancel();
  const utterance = new SpeechSynthesisUtterance(text);
  utterance.lang = languageVoiceMap[currentLanguage] || languageVoiceMap[fallbackLanguage];
  utterance.pitch = 1;
  utterance.rate = currentLanguage === 'zh' ? 0.95 : 1;
  currentUtterance = utterance;
  window.speechSynthesis.speak(utterance);
};

if (speakButton) {
  if (!speechSupported) {
    speakButton.disabled = true;
    speakButton.title = 'Text-to-speech is not supported in this browser.';
  } else {
    speakButton.addEventListener('click', () => {
      if (window.speechSynthesis.speaking) {
        window.speechSynthesis.cancel();
        currentUtterance = null;
        return;
      }
      speakArtifactDescription();
    });
  }
}
